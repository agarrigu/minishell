int	ft_isopp(int c)
{
	return (c == '>' || c == '<' || c == '!' || c == '$' || c == '|'
		|| c == '\'' || c == '"');
}

int	ft_isnonquoteopp(int c)
{
	return (c == '>' || c == '<' || c == '!' || c == '$' || c == '|');
}

int	ft_isquoteopp(int c)
{
	return (c == '\'' || c == '"');
}
