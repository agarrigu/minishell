#include "ft.h"

uint64_t	hash_key(const char *key)
{
	uint64_t	hash;

	hash = FNV_OFFSET;
	while (*key)
	{
		hash ^= (uint64_t)(*key);
		hash *= FNV_PRIME;
		++key;
	}
	return (hash);
}
